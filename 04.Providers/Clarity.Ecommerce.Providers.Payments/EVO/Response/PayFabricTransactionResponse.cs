namespace Clarity.Ecommerce.Providers.Payments.EVO
{
    using JetBrains.Annotations;

    /// <summary>A pay fabric transaction response.</summary>
    [PublicAPI]
    public class PayFabricTransactionResponse
    {
        /// <summary>String PayFabric transaction key. This is generated upon creation of the transaction in the
        /// PayFabric server.   varchar(64)</summary>
        /// <value>The trx key.</value>
        public string? TrxKey { get; set; }

        /// <summary>String Authorization code. Generated by the Payment Gateway.   varchar(64)</summary>
        /// <value>The authentication code.</value>
        public string? AuthCode { get; set; }

        /// <summary>String Status of transaction. Generated by the Payment Gateway. Possible values are Approved, Denied,
        /// Declined, Failure, AVSFailure, and More Info.   varchar(16)</summary>
        /// <value>The status.</value>
        public string? Status { get; set; }

        /// <summary>String Result code of the transaction. Generated by the Payment Gateway.   varchar(32)</summary>
        /// <value>The result code.</value>
        public string? ResultCode { get; set; }

        /// <summary>String Result message of the transaction. Generated by the Payment Gateway.    nvarchar(512)</summary>
        /// <value>The message.</value>
        public string? Message { get; set; }

        /// <summary>String Unique identifier for the transaction. Generated by the Payment Gateway.    varchar(128)</summary>
        /// <value>The identifier of the origination.</value>
        public string? OriginationID { get; set; }

        /// <summary>String Date of transaction. It's format should like "3/23/2015 11:16:19 PM".   datetime.</summary>
        /// <value>The trx date.</value>
        public string? TrxDate { get; set; }

        /// <summary>String A tag value which is generated by FirstData, which must be included when submitting a
        /// transaction to FirstData. This is only valid if FirstData GGe4 is the Payment Gateway. varchar(32)</summary>
        /// <value>The resp trx tag.</value>
        public object? RespTrxTag { get; set; }

        /// <summary>String Verification status code of CVC code. Generated by the Payment Gateway. nvarchar(32)</summary>
        /// <value>The cvv 2 response.</value>
        public string? CVV2Response { get; set; }

        /// <summary>String Verification status code of zip code. Generated by the Payment Gateway. nvarchar(32)</summary>
        /// <value>The avs zip response.</value>
        public object? AVSZipResponse { get; set; }

        /// <summary>String Verification status code of billing address. Generated by the Payment Gateway.  nvarchar(32)</summary>
        /// <value>The avs address response.</value>
        public object? AVSAddressResponse { get; set; }

        /// <summary>String Flag indicating that address verification has been ignored because the address is an
        /// international address. Generated by the Payment Gateway.    nvarchar(32)</summary>
        /// <value>The iavs address response.</value>
        public string? IAVSAddressResponse { get; set; }
    }
}
