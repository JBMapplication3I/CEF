        private Dictionary<string, string> CreateQueryParams(
            bool? onlyData = null,
            int? limit = null,
            string[]? fields = null,
            (string field, string query)? fieldQuery = null)
        {
            Dictionary<string, string> queryParams = new Dictionary<string, string>();
            if (Contract.CheckNotNull(onlyData))
            {
                queryParams.Add(OnlyData, "true");
            }
            if (Contract.CheckNotNull(limit) && Contract.CheckValidID(limit))
            {
                queryParams.Add(Limit, limit.ToString());
            }
            if (Contract.CheckNotEmpty(fields))
            {
                queryParams.Add(Fields, string.Join(",", fields));
            }
            if (Contract.CheckNotNull(fieldQuery))
            {
                queryParams.Add(FieldQuery, $"{fieldQuery!.Value.field}={fieldQuery!.Value.query}");
            }
            return queryParams;
        }

        private (string resource, string overrideUrl)? BuildURLAndResourceForFusionRequest(string splitOn, int skip, int take, List<Link>? links)
        {
            var urlPieces = links.SingleOrDefault(x => x.Name == splitOn).Href?.Split('/');
            if (urlPieces is null || urlPieces!.Count() is not > 0)
            {
                return null;
            }
            return (string.Join("/", urlPieces.Skip(urlPieces.Count() - skip)).TrimStart('/'),
                string.Join("/", urlPieces.Take(urlPieces.Count() - take)).TrimEnd('/'));
        }

        private async Task<T?> GetResponseAsync<T>(
                string resource,
                Dictionary<string, string>? queryParams = null,
                T? payload = null,
                Method method = Method.GET,
                string? overrideUrl = null)
            where T : class, new()
        {
            var serializer = new JsonSerializer { NullValueHandling = NullValueHandling.Ignore };
            var ser = new RestSharpSerializer();
            var req = new RestRequest
            {
                Method = method,
                Resource = resource,
            };
            if (payload is not null)
            {
                var writer = new StringWriter();
                serializer.Serialize(writer, payload);
#pragma warning disable CS0618 // Type or member is obsolete
                req.Body = new RequestBody("application/json", string.Empty, writer.ToString());
#pragma warning restore CS0618 // Type or member is obsolete
                await writer.FlushAsync();
            }
            if (queryParams is not null)
            {
                foreach (var kv in queryParams)
                {
                    req.AddQueryParameter(kv.Key, kv.Value, false);
                }
            }
            if (Client is null
                || (!string.IsNullOrWhiteSpace(overrideUrl)
                    && Client is not null
                    && Client.BaseUrl!.ToString() != overrideUrl))
            {
                Client = GenerateClient(overrideUrl);
            }
            var res = await Client!.ExecuteAsync(req);
            try
            {
                return ser.Deserialize<T>(res);
            }
            catch (Exception)
            {
                return null;
            }
        }

        private RestClient GenerateClient(string? overrideUrl = null)
        {
            return new RestClient
            {
                Authenticator = new HttpBasicAuthenticator(JBMConfig.JBMFusionUsername, JBMConfig.JBMFusionPassword),
                BaseUrl = overrideUrl is null ? new(JBMConfig.JBMFusionBaseURL) : new(overrideUrl),
            };
        }
